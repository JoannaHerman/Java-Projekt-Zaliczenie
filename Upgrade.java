public class Upgrade {
    void addWheels(Vehicle bicycle, int additionalWheels){

        bicycle.wheels= bicycle.wheels+additionalWheels;

    }
    void addHeight(Boat boat, double additionalHeight){

        boat.sail_height= boat.sail_height+additionalHeight;

    }
    void addPower(Engine engine, int additionalPower){
        engine.power= engine.power+additionalPower;
    }
}
